// How often to perform periodic event (5 seconds or 5000ms)
#define SBP_PERIODIC_EVT_PERIOD                         5000

// How often to pulse the LED at the beginning of long-term sampling
#define SBP_LONGTERM_UPDATE_EVT_PERIOD_MINS             5
#define MS_PER_MIN                                      60000
#define SBP_LONGTERM_UPDATE_EVT_PERIOD                  SBP_LONGTERM_UPDATE_EVT_PERIOD_MINS * MS_PER_MIN       
   
// How often to pulse the test GPIO to fake interrupt (500Hz or 2ms)
#define SBP_TEST_INT_EVT_PERIOD                         2       

// What is the advertising interval when device is discoverable (units of 625us, 160=100ms)
#define DEFAULT_ADVERTISING_INTERVAL          160

// Limited discoverable mode advertises for 30.72s, and then stops
// General discoverable mode advertises indefinitely

#if defined ( CC2540_MINIDK )
#define DEFAULT_DISCOVERABLE_MODE             GAP_ADTYPE_FLAGS_LIMITED
#else
#define DEFAULT_DISCOVERABLE_MODE             GAP_ADTYPE_FLAGS_GENERAL
#endif  // defined ( CC2540_MINIDK )

// Minimum connection interval (units of 1.25ms, 80=100ms) if automatic parameter update request is enabled
#define DEFAULT_DESIRED_MIN_CONN_INTERVAL     80

// Maximum connection interval (units of 1.25ms, 800=1000ms) if automatic parameter update request is enabled
#define DEFAULT_DESIRED_MAX_CONN_INTERVAL     800

// Slave latency to use if automatic parameter update request is enabled
#define DEFAULT_DESIRED_SLAVE_LATENCY         0

// Supervision timeout value (units of 10ms, 1000=10s) if automatic parameter update request is enabled
#define DEFAULT_DESIRED_CONN_TIMEOUT          1000

